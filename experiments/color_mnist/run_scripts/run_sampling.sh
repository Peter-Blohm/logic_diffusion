#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
COLOR_MNIST_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"

COLOR_MNIST_PYTHON="${COLOR_MNIST_PYTHON:-python3}"
COLOR_MNIST_DEFAULT_CNF="${COLOR_MNIST_DEFAULT_CNF:-[[1]]}"
COLOR_MNIST_DEFAULT_GAMMA="${COLOR_MNIST_DEFAULT_GAMMA:-1}"
COLOR_MNIST_DEFAULT_COMPOSITION="${COLOR_MNIST_DEFAULT_COMPOSITION:-dombi}"
COLOR_MNIST_DEFAULT_RESAMPLE="${COLOR_MNIST_DEFAULT_RESAMPLE:-0}"
COLOR_MNIST_DEFAULT_BATCH_SIZE="${COLOR_MNIST_DEFAULT_BATCH_SIZE:-64}"
COLOR_MNIST_DEFAULT_NUM_STEPS="${COLOR_MNIST_DEFAULT_NUM_STEPS:-500}"
COLOR_MNIST_DEFAULT_OUTDIR="${COLOR_MNIST_DEFAULT_OUTDIR:-$COLOR_MNIST_DIR/runs}"

if [[ $# -gt 0 ]]; then
  "$COLOR_MNIST_PYTHON" "$COLOR_MNIST_DIR/sample_cnf_compositions.py" "$@"
  exit 0
fi

"$COLOR_MNIST_PYTHON" "$COLOR_MNIST_DIR/sample_cnf_compositions.py" \
  --cnf "$COLOR_MNIST_DEFAULT_CNF" \
  --gamma "$COLOR_MNIST_DEFAULT_GAMMA" \
  --resample "$COLOR_MNIST_DEFAULT_RESAMPLE" \
  --composition "$COLOR_MNIST_DEFAULT_COMPOSITION" \
  --batch_size "$COLOR_MNIST_DEFAULT_BATCH_SIZE" \
  --num_steps "$COLOR_MNIST_DEFAULT_NUM_STEPS" \
  --outdir "$COLOR_MNIST_DEFAULT_OUTDIR"
